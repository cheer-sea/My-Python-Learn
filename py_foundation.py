## 列表切片
list_2 = ['a', 'b', 'd', 'f', 'g', 'h', 'j', 'k', 'l']
print(list_2[ : : 2]) # 步长为2
print(list_2[2: -2: 3]) # 切片,去除第2个元素及以前,倒数第2个元素及以后的所有元素,之后步长为3

list_2 + ['1'] # 列表拼接,将'1'添加到list_2的末尾,注意中括号




