### 变量类型
## 常用
# str （字符串,使用单引号或双引号,当为f字符串时,使用大括号可以引用其他变量）
str_1 = 'A try'
str_2 = f'{str_1}, then, a new try'
# int or float （数字）
num_1 = 10
num_2 = 1.54
# bool （布尔值）
bool_1 = True
bool_2 = False
# set （集合,使用大括号,无序不重复元素）
set_1 = {1, 2, 3}
# tuple (元组,使用小括号,有序不重复元素)
tuple_1 = (1, 2, 3)
# list (列表,使用方括号,有序可重复元素)
list_1 = [1, 2, 3, 3, 4, 5]
# dict (字典,使用花括号,无序不重复键值对)
dict_1= {'name': 'Alice', 'age': 20}

